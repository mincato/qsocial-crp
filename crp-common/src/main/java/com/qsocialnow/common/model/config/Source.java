package com.qsocialnow.common.model.config;

public class Source {

    private Long id;

    private boolean manual;

    public Source() {
        this.id = id;
        this.manual = manual;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public boolean isManual() {
        return manual;
    }

    public void setManual(boolean manual) {
        this.manual = manual;
    }

}
